fn ensure_project_structure(root: &Path) -> Result<()> {
    fs::create_dir_all(root.join("specs"))?;
    fs::create_dir_all(root.join("src"))?;
    // fs::create_dir_all(root.join("tests"))?; // Optional
    Ok(())
}

/// Scaffold a new project with Spec Kit structure
pub struct SpecScaffoldSkill;

#[async_trait]
impl Skill for SpecScaffoldSkill {
    fn id(&self) -> &'static str {
        "spec_scaffold"
    }

    fn name(&self) -> &'static str {
        "Spec Scaffold"
    }

    fn description(&self) -> &'static str {
        "Scaffold a new project with Spec Kit structure (native Rust)"
    }

    fn permission_level(&self) -> PermissionLevel {
        PermissionLevel::Safe // Only creates files
    }

    fn modes(&self) -> &'static [Mode] {
        &[Mode::Build]
    }

    async fn execute(&self, input: SkillInput, ctx: &SkillContext) -> Result<SkillOutput> {
        let name = input
            .params
            .get("name")
            .and_then(|v| v.as_str())
            .unwrap_or("my-project");

        let project_path = ctx.working_dir.join(name);

        if project_path.exists() {
            return Ok(SkillOutput::text(format!(
                "Directory '{}' already exists. Please choose a different name or use `spec_init` in the existing folder.", 
                name
            )));
        }

        fs::create_dir_all(&project_path)?;
        ensure_project_structure(&project_path)?;

        // Create initial README
        let readme_content = format!(
            "# {}\n\nGenerated by Little Helper Spec Kit.\n\n## Specs\nSee `specs/` for project specifications.\n", 
            name
        );
        fs::write(project_path.join("README.md"), readme_content)?;

        // Create .gitignore
        fs::write(
            project_path.join(".gitignore"),
            "target/\nnode_modules/\n.DS_Store\n",
        )?;

        // Initialize git (Real Git)
        std::process::Command::new("git")
            .arg("init")
            .current_dir(&project_path)
            .output()?;

        let mut suggestions = Vec::new();
        suggestions.push(SuggestedAction {
            label: "Initialize Spec".to_string(),
            skill_id: "spec_init".to_string(),
            params: [("path".to_string(), json!(project_path.to_string_lossy()))].into(),
        });

        Ok(SkillOutput::text(format!(
            "{}\n\nWOOF! I've scavenged a new home for your project at `{}`.\n\nStructure created:\n- `specs/` (Place your ideas here)\n- `src/` (Code goes here)\n- `README.md`\n- `.gitignore`\n\nGit initialized! 🦴",
            SPEC_DOG,
            name
        )))
    }
}

/// Initialize a spec file in an existing project
pub struct SpecInitSkill;

#[async_trait]
impl Skill for SpecInitSkill {
    fn id(&self) -> &'static str {
        "spec_init"
    }

    fn name(&self) -> &'static str {
        "Spec Init"
    }

    fn description(&self) -> &'static str {
        "Create a new specification file"
    }

    fn permission_level(&self) -> PermissionLevel {
        PermissionLevel::Safe
    }

    fn modes(&self) -> &'static [Mode] {
        &[Mode::Build]
    }

    async fn execute(&self, input: SkillInput, ctx: &SkillContext) -> Result<SkillOutput> {
        let name = input
            .params
            .get("name")
            .and_then(|v| v.as_str())
            .unwrap_or("01_initial_spec");

        let path = input
            .params
            .get("path")
            .and_then(|v| v.as_str())
            .map(PathBuf::from)
            .unwrap_or_else(|| ctx.working_dir.clone());

        let specs_dir = path.join("specs");
        fs::create_dir_all(&specs_dir)?;

        let filename = if name.ends_with(".md") {
            name.to_string()
        } else {
            format!("{}.md", name)
        };
        let spec_path = specs_dir.join(&filename);

        if spec_path.exists() {
            return Ok(SkillOutput::text(format!(
                "Spec file `{}` already exists.",
                filename
            )));
        }

        let template = r#"# Feature Specification

## Goal
Describe what we are building.

## Requirements
- [ ] Requirement 1
- [ ] Requirement 2

## Implementation Plan
1. Step 1
2. Step 2
"#;

        fs::write(&spec_path, template)?;

        let mut suggestions = Vec::new();
        suggestions.push(SuggestedAction {
            label: "Implement Spec".to_string(),
            skill_id: "spec_implement".to_string(),
            params: [("spec".to_string(), json!(filename))].into(),
        });

        Ok(SkillOutput::text(format!(
            "{}\n\n*Tail wagging* I've buried a fresh spec template at `specs/{}`. \nFill it out and I'll fetch the code!",
            SPEC_DOG,
            filename
        )))
    }
}

/// Read a spec and prompt the agent to implement it
pub struct SpecImplementSkill;

#[async_trait]
impl Skill for SpecImplementSkill {
    fn id(&self) -> &'static str {
        "spec_implement"
    }

    fn name(&self) -> &'static str {
        "Spec Implement"
    }

    fn description(&self) -> &'static str {
        "Read a spec and implement it (replaces Swarm)"
    }

    fn permission_level(&self) -> PermissionLevel {
        PermissionLevel::Sensitive // Modifies code
    }

    fn modes(&self) -> &'static [Mode] {
        &[Mode::Build]
    }

    async fn execute(&self, input: SkillInput, ctx: &SkillContext) -> Result<SkillOutput> {
        let spec_name = input
            .params
            .get("spec")
            .and_then(|v| v.as_str())
            .unwrap_or("01_initial_spec.md");

        let path = input
            .params
            .get("path")
            .and_then(|v| v.as_str())
            .map(PathBuf::from)
            .unwrap_or_else(|| ctx.working_dir.clone());

        let spec_path = path.join("specs").join(spec_name);

        if !spec_path.exists() {
            // Try adding .md extension
            let spec_path_md = path.join("specs").join(format!("{}.md", spec_name));
            if !spec_path_md.exists() {
                return Ok(SkillOutput::text(format!(
                    "Spec file not found at `specs/{}` or `specs/{}.md`.",
                    spec_name, spec_name
                )));
            }
        }

        let spec_content = fs::read_to_string(&spec_path).unwrap_or_default();

        Ok(SkillOutput::text(format!(
            "{}\n\nI've read `{}`. To start the uninterrupted build loop, I need to create a plan.\n\n---\n{}\n---\n\n**Instructions for Agent:**\n1. Analyze the spec above.\n2. Break it down into 3-5 granular implementation tasks.\n3. Create a `specs/status.json` file using the `write_file` skill content roughly like:\n```json\n{{\n  \"spec_file\": \"{}\",\n  \"tasks\": [\n    {{ \"id\": \"1\", \"title\": \"Setup\", \"status\": \"Pending\", ... }},\n    {{ \"id\": \"2\", \"title\": \"Core Logic\", \"status\": \"Pending\", ... }}\n  ]\n}}\n```\n4. Then call `spec_next_task` to start the loop.",
            SPEC_DOG,
            spec_name,
            spec_content,
            spec_name
        )))
    }
}

/// Execute the next task in the build plan
pub struct SpecNextTaskSkill;

#[async_trait]
impl Skill for SpecNextTaskSkill {
    fn id(&self) -> &'static str {
        "spec_next_task"
    }

    fn name(&self) -> &'static str {
        "Spec Next Task"
    }

    fn description(&self) -> &'static str {
        "Execute the next pending task in the build plan"
    }

    fn permission_level(&self) -> PermissionLevel {
        PermissionLevel::Sensitive
    }

    fn modes(&self) -> &'static [Mode] {
        &[Mode::Build]
    }

    async fn execute(&self, _input: SkillInput, ctx: &SkillContext) -> Result<SkillOutput> {
        use super::spec_tracker::SpecStatus;

        let mut status = match SpecStatus::load(&ctx.working_dir) {
            Ok(s) => s,
            Err(_) => {
                return Ok(SkillOutput::error(
                    "No build plan found. Run `spec_implement` first to create one.",
                ))
            }
        };

        if let Some(task) = status.next_pending_task() {
            // Create a prompt for the agent to execute THIS specific task
            let task_prompt = format!(
                 "**BUILD TASK [{}/{}]**: {}\n\nDescription: {}\n\nExecute this task now. Create/Edit the necessary files. When done, I will automatically mark this task complete and move to the next one.",
                 status.current_task_index + 1,
                 status.tasks.len(),
                 task.title,
                 task.description
             );

            // Advance the tracker *optimistically* (or we could wait for a confirmation skill, but let's loop)
            status.complete_current_task();
            status.save(&ctx.working_dir)?;

            let mut suggestions = Vec::new();
            if !status.completed {
                suggestions.push(SuggestedAction {
                    label: "Next Task".to_string(),
                    skill_id: "spec_next_task".to_string(),
                    params: HashMap::new(),
                });
            }

            Ok(SkillOutput::text(task_prompt)
                .with_suggested_action("spec_next_task", HashMap::new()))
        } else {
            Ok(SkillOutput::text(format!(
                "{}\n\nAll tasks completed! Build finished successfully. 🏗️✅",
                SPEC_DOG
            )))
        }
    }
}
